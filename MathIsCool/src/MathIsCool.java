
public class MathIsCool {
	public static void main(String[] args){
		int firstNum = 6;
		int secondNum= -7;
		
		//this will print out the greater of the 2 numbers
		System.out.println(Math.max(firstNum, secondNum));
		
		//this will print out the smaller of the 2 numbers
		System.out.println(Math.min(firstNum, secondNum));
		
		//this will calculate the absolute value of the secondNum
		System.out.println(Math.abs (secondNum));
		
		//this will calculate the firstNum to power of secondNum
		System.out.println(Math.pow(firstNum, secondNum));
		
		//something that has the value of pi
		System.out.println(Math.PI);
		
		//this will take square root of firstNum
		System.out.println(Math.sqrt(firstNum));
	}
}
